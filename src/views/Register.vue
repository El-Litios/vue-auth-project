<template>
    <div>
        <h1>Registro</h1>
        <form @submit.prevent="createUser({email: email, password: pass})">
            <input type="email" v-model="email" placeholder="Correo">
            <input type="password" v-model="pass" placeholder="ContraseÃ±a">
            <input type="password" v-model="repeatedpass" placeholder="Repita ContraseÃ±a">
            <button :disabled="!disableButton" type="submit">Registrarse</button>
        </form>
    </div>
</template>

<script>
import {mapActions, mapState} from 'vuex'

export default {
    name: 'Registro',
    data() {
        return {
            email: '',
            pass: '',
            repeatedpass: ''
        }
    },

    methods: {
        ...mapActions(['createUser'])
    },

    computed: {
        ...mapState(['error']),
        disableButton(){
            return this.pass === this.repeatedpass && this.pass.trim() !== '' && this.pass.length > 5
        }
    }
}
</script>